GAME_OVER_IMAGE = None

current_direction = MOVE_NONE

def change_direction(e):
    global KEY_UP, KEY_DOWN, KEY_LEFT, KEY_RIGHT
    global MOVE_UP, MOVE_DOWN, MOVE_LEFT, MOVE_RIGHT
    global current_direction
    
    keycode = e.keycode

    if (KEY_UP == keycode and current_direction != MOVE_DOWN):
        current_direction = MOVE_UP
    elif (KEY_DOWN == keycode and current_direction != MOVE_UP):
        current_direction = MOVE_DOWN
    elif (KEY_LEFT == keycode and current_direction != MOVE_RIGHT):
        current_direction = MOVE_LEFT
    elif (KEY_RIGHT == keycode and current_direction != MOVE_LEFT):
        current_direction = MOVE_RIGHT

def draw_border(canvas: Canvas):
    global BORDER_COLOR
    leftX = 1
    topY = 1
    rightX = canvas.winfo_width() - 2
    bottomY = canvas.winfo_height() - 2
    # top
    canvas.create_line(leftX, topY, rightX, topY, fill = BORDER_COLOR)
    # right
    canvas.create_line(rightX, topY, rightX, bottomY, fill = BORDER_COLOR)
    # bottom
    canvas.create_line(leftX, bottomY, rightX, bottomY, fill = BORDER_COLOR)
    # left
    canvas.create_line(leftX, topY, leftX, bottomY, fill = BORDER_COLOR)

def create_snake_part(x: int, y: int) -> list:
    return [x, y, None]

def initiate_snake(size: int = 3) -> list:
    parts = list()
    for i in range(size):
        parts.insert(0, create_snake_part(i, 0))

    return parts

def add_tail(snake: list):
    tail = snake[len(snake) - 1]
    snake.append(create_snake_part(tail[0], tail[1]))
    
def get_coords_for_block(part: list, margin: int = 1) -> tuple:
    global BLOCK_SIZE
    x0 = part[0] * BLOCK_SIZE
    y0 = part[1] * BLOCK_SIZE
    x1 = x0 + BLOCK_SIZE - margin
    y1 = y0 + BLOCK_SIZE - margin

    return (x0 + margin, y0 + margin, x1 - margin, y1 - margin)

def draw_snake(canvas: Canvas, snake: list):
    global SNAKE_COLOR, BLOCK_SIZE
    for part in snake:
        if (not part[2]):
            c = get_coords_for_block(part)
            part[2] = canvas.create_rectangle(c[0], c[1], c[2], c[3], 
                                              fill = SNAKE_COLOR)

def redraw_head(canvas: Canvas, snake: list):
    global BLOCK_SIZE
    head = snake[0]
    c = get_coords_for_block(head)
    canvas.coords(head[2], c[0], c[1], c[2], c[3])

def move(direction: int, snake: list):
    global MOVE_UP, MOVE_DOWN, MOVE_LEFT, MOVE_RIGHT

    tail = snake.pop()
    head = snake[0]

    snake.insert(0, tail)
    new_head = snake[0]
    new_head[0] = head[0]
    new_head[1] = head[1]

    if (MOVE_UP == direction):
        new_head[1] = head[1] - 1
    elif (MOVE_DOWN == direction):
        new_head[1] = head[1] + 1
    elif (MOVE_LEFT == direction):
        new_head[0] = head[0] - 1
    elif (MOVE_RIGHT == direction):
        new_head[0] = head[0] + 1

def get_random_coords() -> tuple:
    global FIELD_HORIZONTAL_SIZE, FIELD_VERTICAL_SIZE

    return (randint(0, FIELD_HORIZONTAL_SIZE - 1), 
            randint(0, FIELD_VERTICAL_SIZE - 1))

def get_random_coords_outside_of_snake(snake: list) -> tuple:
    coords = get_random_coords()

    for part in snake:
        if (part[0] == coords[0] and part[1] == coords[1]):
            return get_random_coords_outside_of_snake(snake)

    return coords

def initiate_food(snake: list) -> list:
    food = [0, 0, None]
    spawn_food(food, snake)
    return food

def spawn_food(food: list, snake: list):
    c = get_random_coords_outside_of_snake(snake)
    food[0] = c[0]
    food[1] = c[1]

def draw_food(canvas: Canvas, food: list):
    global FOOD_COLOR, BLOCK_SIZE
    c = get_coords_for_block(food)
    food[2] = canvas.create_rectangle(c[0], c[1], c[2], c[3], fill = FOOD_COLOR)

def redraw_food(canvas: Canvas, food: list):
    c = get_coords_for_block(food)
    canvas.coords(food[2], c[0], c[1], c[2], c[3])

def collides_border(snake: list) -> bool:
    global FIELD_HORIZONTAL_SIZE, FIELD_VERTICAL_SIZE
    head = snake[0]
    return head[0] < 0 or \
           head[0] > FIELD_HORIZONTAL_SIZE - 1 or \
           head[1] < 0 or \
           head[1] > FIELD_VERTICAL_SIZE - 1

def collides_itself(snake: list) -> bool:
    head = snake[0]

    for index, part in enumerate(snake):
        if (0 == index):
            continue

        if (part[0] == head[0] and part[1] == head[1]):
            return True

    return False

def collides_something(snake: list):
    return collides_border(snake) or collides_itself(snake)

def ate_food(snake: list, food: list) -> bool:
    head = snake[0]

    return head[0] == food[0] and head[1] == food[1]

def clear_snake(canvas: Canvas, snake: list):
    for part in snake:
        canvas.delete(part[2])

    snake.clear()

def get_game_over_image() -> ImageTk.PhotoImage:
    global GAME_OVER_IMAGE_BIN
    image_stream = io.BytesIO(b64decode(GAME_OVER_IMAGE_BIN))
    image = Image.open(image_stream)
    return ImageTk.PhotoImage(image)

def game_over(canvas: Canvas, root: Tk):
    global GAME_OVER_IMAGE
    canvas.delete('all')

    if (not GAME_OVER_IMAGE):
        GAME_OVER_IMAGE = get_game_over_image()

    canvas.create_image(0, 0, image = GAME_OVER_IMAGE, anchor = 'nw')

    root.update()
    root.bind('<KeyPress>', lambda e: init_game(canvas, root))

def get_delay() -> float:
    global SPEED
    return SPEED * 10

def loop(root: Tk, canvas: Canvas, snake: list, food: list):
    move(current_direction, snake)

    if (ate_food(snake, food)):
        add_tail(snake)
        draw_snake(canvas, snake)
        spawn_food(food, snake)
        redraw_food(canvas, food)

    if (collides_something(snake)):
        game_over(canvas, root)
        return
    else:
        redraw_head(canvas, snake)

    root.after(get_delay(), lambda: loop(root, canvas, snake, food))

def init_game(canvas: Canvas, root: Tk):
    global current_direction

    current_direction = MOVE_RIGHT

    root.unbind_all('<KeyPress>')
    root.bind('<KeyPress>', change_direction)
    canvas.pack()
    root.update()

    canvas.delete('all')

    snake = initiate_snake()
    food = initiate_food(snake)

    draw_border(canvas)
    draw_snake(canvas, snake)
    draw_food(canvas, food)
    
    loop(root, canvas, snake, food)

init_game(canvas, root)

root.mainloop()